#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;

	if (!(s = malloc(sizeof(char) * (size + 1))))
		return (NULL);
	while (size + 1 > 0)
	{
		s[size] = 0;
		size--;
	}
	return (s);
}
